package ForLoop;

public class Print10to1 {

	public static void main(String[] args) 
	{
		System.out.println(".....Desc.....");
		for(int j=10; j>=1; j--)
		{
			System.out.println(j);
		}
	}

}

/*
 * While - When the no.of iteration is not known
 * For - When the no.of iteration is known
 * Do..While - When you want to print the output atleast once
 */
